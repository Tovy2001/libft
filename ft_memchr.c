#include "libft.h"

void	*memchr(const void *s, int c, size_t n)
{
	unsigned char	*p;
	unsigned int	i;

	p = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (p[i] == (unsigned char)c)
			return ((void *)p + 1);
		i++;
	}
	return (NULL);
}
